    private void tuserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tuserMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tuser.getModel();

        try {
            String pass = String.valueOf(txtpass.getPassword());
            int fila = tuser.getSelectedRow();
            int id = Integer.parseInt(tuser.getValueAt(fila, 0).toString());
            Date date = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").parse((String) model.getValueAt(fila, 4));
            FR.setDate(date);

            PreparedStatement ps;
            ResultSet rs;
            ps = con.prepareStatement("select * from usuarios_nomina where id_Usuario_N =?");
            ps.setInt(1, id);
            rs = ps.executeQuery();
            java.sql.Statement st = con.createStatement();
            while (rs.next()) {
                txtid1.setText(String.valueOf(id));
                txtname.setText(rs.getString("Nombre(s)_N"));
                txtAP.setText(rs.getString("Apellido_P_N"));
                txtAM.setText(rs.getString("Apellido_M_N"));
                txtuser.setText(rs.getString("Usuario_N"));
                txtpass.setText(rs.getString("Pass_N"));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());

        } catch (ParseException ex) {
            Logger.getLogger(Usuarios_Nomina_3.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tuserMouseClicked

